var MongoDb = require("mongodb")
    , MongoClient = MongoDb.MongoClient
    , format = require('util').format
    , config = require('../conf')
    , log = require('../lib/log')(module);

var mongoClient = new MongoClient();


var drv = function (option) {

};

drv._initDB = function (db) {
    this.db = db;
    return this.db;
};

mongoClient.connect(config.get('mongodb:uri') ,function(err, db) {
    if (err) {
        log.error('Connect db error: %s', err.message);
        throw err;
    };
    drv._initDB(db);

    drv.dialplanCollection = db.collection(config.get("mongodb:collectionPublic"));
    drv.globalCollection = db.collection(config.get("mongodb:collectionSystem"));
    log.info('Connected db %s ', config.get('mongodb:uri'));
    db.on('close', function () {
        log.error('close MongoDB');
    });
});

drv.getCollection = function (name) {
    try {
        return this.db.collection(name)
    } catch (e) {
        log.error(e.message);
    }
};

module.exports = drv;